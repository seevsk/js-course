// Initialize variable with a value
var product = "24 inch monitor";
console.log(product);

// Variables can be reassigned
product = "19 inch monitor";
console.log(product);

// JavaScript is a dynamic
// language, it does not specify a data type.
product = 24;
console.log(product);

// Variables can be “initialized without
// value” and “assigned later”.
var available;
available = true;
available = false;

// Initialize multiple variables
