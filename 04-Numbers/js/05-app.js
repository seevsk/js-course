
let score = 5;

let postIncrement = score++;
console.log(postIncrement);
// The value is "6" remember it is obtained later. ↑

let preIncrement = --score;
console.log(preIncrement);

// Compound Assignment Operators ↓

score += 3; // score = score + 3;
console.log (score)

score -= 3; // score = score - 3;
console.log (score)
